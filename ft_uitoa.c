#include "libftprintf.h"

char	*ft_uitoa(unsigned int n)
{
	char	buffer[11];
	int		i;

	buffer[10] = '\0';
	i = 10;

	if (n == 0)
		buffer[--i] = '0';

	while (n > 0)
	{
		i--;
		buffer[i] = (n % 10) + '0';
		n = n / 10;
		
	}

	return (ft_strdup(&buffer[i]));
	
}